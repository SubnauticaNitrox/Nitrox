namespace uTinyRipper.Classes.QualitySettingss
{
	/// <summary>
	/// Determines which type of shadows should be used.
	/// </summary>
	public enum ShadowQuality
	{
		/// <summary>
		/// Disable Shadows.
		/// </summary>
		Disable		= 0,
		/// <summary>
		/// Hard Shadows Only.
		/// </summary>
		HardOnly	= 1,
		/// <summary>
		/// Hard and Soft Shadows.
		/// </summary>
		All			= 2,
	}
}
